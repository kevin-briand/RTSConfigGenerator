package fr.firedream.RtsConfigGenerator;

public class Entity {
    private String name;
    private String id;

    public Entity(String name, String id) {
        this.name = name;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getId() {
        return id;
    }
}
